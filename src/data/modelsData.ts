const generateMetricsTrend = (baseValue: number, days: number = 7): MetricTrend[] => {
  const trend: MetricTrend[] = [];
  for (let i = days; i >= 0; i--) {
    const date = subDays(new Date(), i);
    const variation = (Math.random() - 0.5) * 2;
    trend.push({
      timestamp: format(date, 'MMM dd'),
      value: Math.min(100, Math.max(0, baseValue + variation)),
    });
  }
  return trend;
};

